    private void btn_buscarTexto2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarTexto2ActionPerformed
        this.disableFields();

        if (!textoBuscar.getText().equals("")) {
            C_Categoria ccate = new C_Categoria();
            mc.Clear();
            List<Categoria> lista = ccate.busquedaporNombre(textoBuscar.getText());

            for(Categoria cate : lista){
                mc.addRow(cate);
            }

        }else{
            if (jTable1.getRowCount()< ccat.findAll().size()) {
                for(Categoria cate : ccat.findAll() ){
                    mc.addRow(cate);
                }
            }

        }

    }//GEN-LAST:event_btn_buscarTexto2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int NClic = evt.getClickCount();

        if (NClic >= 2) {
            idCategoria = (Integer) mc.getValueAt(jTable1.getSelectedRow(), 0);
            txt_nombreCategoria1.setText((String) mc.getValueAt(jTable1.getSelectedRow(), 1));
            txt_descripcionCategoria1.setText((String) mc.getValueAt(jTable1.getSelectedRow(), 2));

        }
    }//GEN-LAST:event_jTable1MouseClicked


