    private void btn_guardar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar2ActionPerformed

        if(nuevo){

            this.enableFields();
            JOptionPane.showMessageDialog(null,ccat.create(txt_nombreCategoria1.getText(), txt_descripcionCategoria1.getText()));
            mc.fireTableDataChanged();
            this.clearFields();
            mc.Clear();
            this.nuevo = false;     this.editar = false;
            jTable1.setModel(mc);
        }
        if(editar){
            this.enableFields();

            JOptionPane.showMessageDialog(null, ccat.update(idCategoria,txt_nombreCategoria1.getText(), txt_descripcionCategoria1.getText()));
            mc.fireTableDataChanged();

            this.clearFields();
            this.nuevo = false;     this.editar = false;
        }

    }//GEN-LAST:event_btn_guardar2ActionPerformed

